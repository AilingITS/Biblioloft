package com.example.biblioloft.firebase.fb_user_books;

public class Libro {
    String descripcionLibro, libroID, nombreLibro, paginasLibro, tipoLibro, imageLibro;

    public String getDescripcionLibro() {
        return descripcionLibro;
    }

    public String getLibroID() {
        return libroID;
    }

    public String getNombreLibro() {
        return nombreLibro;
    }

    public String getPaginasLibro() {
        return paginasLibro;
    }

    public String getTipoLibro() {
        return tipoLibro;
    }

    public String getImageLibro() {
        return imageLibro;
    }
}
